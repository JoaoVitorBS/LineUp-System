    private void btn_CadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_CadastrarActionPerformed
// Adicionando dados do Fornecedor no banco a partir das informações das caixas de texto:
        modelFornecedor.setNomefornecedor(this.txt_Nome_Forne.getText());
        modelFornecedor.setRazaosocial(this.txt_Razao_Forne.getText());
        modelFornecedor.setNomefantasia(this.txt_NomeFant_Forne.getText());
        modelFornecedor.setEndereco(this.txt_End_Forne.getText());
        modelFornecedor.setComplemento(this.txt_Comp_Forne.getText());
        modelFornecedor.setBairro(this.txt_Bairro_Forne.getText());
        modelFornecedor.setCidade(this.txt_Cidade_Forne.getText());
        modelFornecedor.setUf(this.combo_UF_Forne.getSelectedItem().toString());
        modelFornecedor.setCNPJ(this.txt_CNPJ.getText());
        modelFornecedor.setEmail(this.txt_Email_Forne.getText());
        modelFornecedor.setTelefone(this.txt_Fone_Forne.getText());
        modelFornecedor.setCelular(this.txt_Celular_Forne.getText());

        // Condição para testar se as informações foram gravadas no banco:
        if (controllerFornecedor.salvarFornecedorController(modelFornecedor) > 0) {
            JOptionPane.showMessageDialog(this, "Fornecedor cadastrado com sucesso!");
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao tentar cadastrar Fornecedor!", "ERRO", JOptionPane.ERROR_MESSAGE);
        }

        //Setando os campos como nulo, após efetuado o cadastro:
        txt_Nome_Forne.setText(null);
        txt_Razao_Forne.setText(null);
        txt_NomeFant_Forne.setText(null);
        txt_End_Forne.setText(null);
        txt_Comp_Forne.setText(null);
        txt_Bairro_Forne.setText(null);
        txt_Cidade_Forne.setText(null);
        combo_UF_Forne.setSelectedItem(null);
        txt_CNPJ.setText(null);
        txt_Email_Forne.setText(null);
        txt_Fone_Forne.setText(null);
        txt_Celular_Forne.setText(null);
        txt_Id_Forne.setText(null);

        busca_fornecedor();
        Combo_Fornecedor();
    }//GEN-LAST:event_btn_CadastrarActionPerformed

