    private void busca_fornecedor() {
        listaModelFornecedores = controllerFornecedor.getListaFornecedorController();
        DefaultTableModel tabela = (DefaultTableModel) tabelafornecedor.getModel();
        tabela.setNumRows(0);

        int tam_lista = listaModelFornecedores.size();
        for (int i = 0; i < tam_lista; i++) {
            tabela.addRow(new Object[]{
                listaModelFornecedores.get(i).getIdfornecedor(),
                listaModelFornecedores.get(i).getNomefornecedor(),
                listaModelFornecedores.get(i).getRazaosocial(),
                listaModelFornecedores.get(i).getNomefantasia(),
                listaModelFornecedores.get(i).getEndereco(),
                listaModelFornecedores.get(i).getComplemento(),
                listaModelFornecedores.get(i).getBairro(),
                listaModelFornecedores.get(i).getCidade(),
                listaModelFornecedores.get(i).getUf(),
                listaModelFornecedores.get(i).getCNPJ(),
                listaModelFornecedores.get(i).getEmail(),
                listaModelFornecedores.get(i).getTelefone(),
                listaModelFornecedores.get(i).getCelular(),});
        }
    }

    
    private void Combo_Fornecedor() {
        listaModelFornecedores = controllerFornecedor.getListaFornecedorController();
        Tela_Produtos.combo_Fornecedor.removeAllItems();
        for (int i = 0; i < listaModelFornecedores.size(); i++) {
            Tela_Produtos.combo_Fornecedor.addItem(listaModelFornecedores.get(i).getNomefantasia());
        }
    }
    
