    private void btn_ApagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ApagarActionPerformed
        // TODO add your handling code here:
        int del = tabelafornecedor.getSelectedRow();
        int id_forne = (int) tabelafornecedor.getValueAt(del, 0);
        int confirma = JOptionPane.showConfirmDialog(null, "Deseja deletar este Fornecedor?", "Atenção", JOptionPane.YES_NO_OPTION);
        if (confirma == JOptionPane.YES_OPTION) {
            //Condição para testar se houve sucesso ou erro na exclusão:
            try {
                if (controllerFornecedor.excluirFornecedorController(id_forne)) {
                    JOptionPane.showMessageDialog(this, "Fornecedor deletado com sucesso!");
                } else {
                    JOptionPane.showMessageDialog(this, "Erro ao tentar deletar fornecedor!", "ERRO", JOptionPane.ERROR_MESSAGE);
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }

        //Setando os campos como nulo
        txt_Nome_Forne.setText(null);
        txt_Razao_Forne.setText(null);
        txt_NomeFant_Forne.setText(null);
        txt_End_Forne.setText(null);
        txt_Comp_Forne.setText(null);
        txt_Bairro_Forne.setText(null);
        txt_Cidade_Forne.setText(null);
        combo_UF_Forne.setSelectedItem(null);
        txt_CNPJ.setText(null);
        txt_Email_Forne.setText(null);
        txt_Fone_Forne.setText(null);
        txt_Celular_Forne.setText(null);

        busca_fornecedor();
    }//GEN-LAST:event_btn_ApagarActionPerformed

