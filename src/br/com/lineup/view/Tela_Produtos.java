    private void btn_ApagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ApagarActionPerformed
        // Apagar dados do produto a partir do ID:
        int del = tabelaproduto.getSelectedRow();
        int id_prod = (int) tabelaproduto.getValueAt(del, 0);
        int confirma = JOptionPane.showConfirmDialog(null, "Deseja deletar este Produto?", "Atenção", JOptionPane.YES_NO_OPTION);
        if (confirma == JOptionPane.YES_OPTION) {
            //Condição para testar se houve sucesso ou erro na exclusão:
            try {
                if (controllerProduto.excluirProdutoController(id_prod)) {
                    JOptionPane.showMessageDialog(this, "Produto deletado com sucesso!");
                } else {
                    JOptionPane.showMessageDialog(this, "Erro ao tentar deletar produto!", "ERRO", JOptionPane.ERROR_MESSAGE);
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Este produto não pode ser deletado, pois já está vinculado a uma Venda!");
            }
        }
        // Setar campos como nulo:
        txt_ID_prod.setText(null);
        txt_Cod_Barras_Prod.setText(null);
        txt_Nome_Prod.setText(null);
        txt_Qtde_Prod.setText(null);
        txt_PreçoCusto_Prod.setText(null);
        txt_PreçoVenda_Prod.setText(null);
        txt_Marca_Prod.setText(null);
        combo_Fornecedor.setSelectedItem(null);
        combo_Grupo.setSelectedItem(null);
        combo_Unidade.setSelectedItem(null);

        // Chamar o método de busca de clientes da tabela:
        busca_produto();

        btn_Cadastrar.setEnabled(true);
    }//GEN-LAST:event_btn_ApagarActionPerformed

