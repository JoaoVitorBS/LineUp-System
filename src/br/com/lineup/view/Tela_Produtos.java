    private void btn_CadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_CadastrarActionPerformed
        // TODO add your handling code here:
        modelProduto.setNomeproduto(this.txt_Nome_Prod.getText());
        modelProduto.setCodbarras(this.txt_Cod_Barras_Prod.getText());
        modelProduto.setQuantidade(Integer.parseInt(this.txt_Qtde_Prod.getText()));
        modelProduto.setUnidadecomercial(this.combo_Unidade.getSelectedItem().toString());
        modelProduto.setGrupo(this.combo_Grupo.getSelectedItem().toString());
        modelProduto.setMarca(this.txt_Marca_Prod.getText());
        modelProduto.setFornecedor(this.combo_Fornecedor.getSelectedItem().toString());
        modelProduto.setPrecovenda(Double.parseDouble(this.txt_PreçoVenda_Prod.getText()));
        modelProduto.setPrecocusto(Double.parseDouble(this.txt_PreçoCusto_Prod.getText()));

        if (controllerProduto.salvarProdutoController(modelProduto) >= 0) {
            JOptionPane.showMessageDialog(this, "Produto cadastrado com sucesso!");
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao cadastrar Produto!", "ERRO", JOptionPane.ERROR_MESSAGE);
        }
        // Setar campos como nulo:
        txt_ID_prod.setText(null);
        txt_Cod_Barras_Prod.setText(null);
        txt_Nome_Prod.setText(null);
        txt_Qtde_Prod.setText(null);
        txt_PreçoCusto_Prod.setText(null);
        txt_PreçoVenda_Prod.setText(null);
        txt_Marca_Prod.setText(null);
        combo_Fornecedor.setSelectedItem(null);
        combo_Grupo.setSelectedItem(null);
        combo_Unidade.setSelectedItem(null);
        
        busca_produto();
    }//GEN-LAST:event_btn_CadastrarActionPerformed

