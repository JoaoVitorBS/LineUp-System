    private void Busca_OS() {
        listaModelOrdem_de_Serviços = controllerOrdem_de_Serviço.getListaOrdem_de_ServiçoController();
        DefaultTableModel tabela = (DefaultTableModel) tabelaOS.getModel();
        tabela.setNumRows(0);

        int tam_lista = listaModelOrdem_de_Serviços.size();
        for (int i = 0; i < tam_lista; i++) {
            tabela.addRow(new Object[]{
                listaModelOrdem_de_Serviços.get(i).getOs(),
                listaModelOrdem_de_Serviços.get(i).getDataos(),
                listaModelOrdem_de_Serviços.get(i).getStatusOS(),
                listaModelOrdem_de_Serviços.get(i).getAparelho(),
                listaModelOrdem_de_Serviços.get(i).getDefeito(),
                listaModelOrdem_de_Serviços.get(i).getServico(),
                listaModelOrdem_de_Serviços.get(i).getValorservico(),
                listaModelOrdem_de_Serviços.get(i).getTecnico(),
                listaModelOrdem_de_Serviços.get(i).getId_cli(),});
        }
    }

    private void Combo_Cliente() {
        listaModelClientes = controllerCliente.getListaClienteController();
        combo_Cliente.removeAllItems();
        for (int i = 0; i < listaModelClientes.size(); i++) {
            combo_Cliente.addItem(listaModelClientes.get(i).getNome_cli());
        }
    }

    private void Combo_Serviço() {
        listaModelServiços = controllerServiços.getListaServiçosController();
        combo_Serviço.removeAllItems();

        for (int i = 0; i < listaModelServiços.size(); i++) {
            combo_Serviço.addItem(listaModelServiços.get(i).getDescricao_servico());
        }
    }

    private void Combo_Técnico() {
        listamodelUsuário_Técnico = controllerUsuário.getListaTécnicoController();
        combo_Técnico.removeAllItems();

        for (int i = 0; i < listamodelUsuário_Técnico.size(); i++) {
            combo_Técnico.addItem(listamodelUsuário_Técnico.get(i).getNomeuser());
        }
    }

    private void Popular_Serviço() {
        if (combo_Serviço.isPopupVisible()) {
            modelServiços = controllerServiços.getServiçosController(combo_Serviço.getSelectedItem().toString());
        }
        txt_ValorServiço.setText(String.valueOf(modelServiços.getValor_servico()));
    }

    private void Popular_Cliente() {
        if (combo_Cliente.isPopupVisible()) {
            modelCliente = controllerCliente.getClienteController(combo_Cliente.getSelectedItem().toString());
        }
        txt_IdCli.setText(String.valueOf(modelCliente.getId_cli()));
    }

