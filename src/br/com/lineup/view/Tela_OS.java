    private void tabelaOSMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaOSMouseClicked
        // TODO add your handling code here:
        int set = tabelaOS.getSelectedRow();
        txt_OS.setText(tabelaOS.getModel().getValueAt(set, 0).toString());
        txt_DataOs.setText(tabelaOS.getModel().getValueAt(set, 1).toString());
        combo_StatusOs.setSelectedItem(tabelaOS.getModel().getValueAt(set, 2).toString());
        txt_Aparelho.setText(tabelaOS.getModel().getValueAt(set, 3).toString());
        txt_Defeito.setText(tabelaOS.getModel().getValueAt(set, 4).toString());
        combo_Serviço.setSelectedItem(tabelaOS.getModel().getValueAt(set, 5).toString());
        txt_ValorServiço.setText(tabelaOS.getModel().getValueAt(set, 6).toString());
        combo_Técnico.setSelectedItem(tabelaOS.getModel().getValueAt(set, 7).toString());
        txt_IdCli.setText(tabelaOS.getModel().getValueAt(set, 8).toString());
        combo_Cliente.setSelectedItem(tabelaOS.getModel().getValueAt(set, 9).toString());

        btn_Cadastrar.setEnabled(true);
    }//GEN-LAST:event_tabelaOSMouseClicked

