    private void btn_ApagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ApagarActionPerformed
        // Apagar dados do cliente a partir do ID:
        int del = tabelacliente.getSelectedRow();
        int id_cli = (int) tabelacliente.getValueAt(del, 0);
        int confirma = JOptionPane.showConfirmDialog(null, "Deseja deletar este Cliente?", "Atenção", JOptionPane.YES_NO_OPTION);
        if (confirma == JOptionPane.YES_OPTION) {
            //Condição para testar se houve sucesso ou erro na exclusão:
            try {
                if (controllerCliente.excluirClienteController(id_cli)) {
                    JOptionPane.showMessageDialog(this, "Cliente deletado com sucesso!");
                } else {
                    JOptionPane.showMessageDialog(this, "Erro ao tentar deletar cliente!", "ERRO", JOptionPane.ERROR_MESSAGE);
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Este cliente não pode ser deletado, pois já está vinculado a uma Ordem de Serviço!");
            }
        }
        // Setar campos como nulo:
        txtclientebusca.setText(null);
        txtclientecpf.setText(null);
        txtclienteemail.setText(null);
        txtclienteendereco.setText(null);
        txtclientefone.setText(null);
        txtclienteid.setText(null);
        txtclientenome.setText(null);
        txtclientebairro.setText(null);
        txtclientecelular.setText(null);
        txtclientecomplemento.setText(null);
        txtclienterg.setText(null);
        comboclienteestado.setSelectedItem(null);
        txtclientecidade.setText(null);

        // Chamar o método de busca de clientes da tabela:
        busca_cliente();

        btn_Cadastrar.setEnabled(true);
    }//GEN-LAST:event_btn_ApagarActionPerformed

