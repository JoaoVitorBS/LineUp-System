    private void btn_CadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_CadastrarActionPerformed
        //Adicionar dados do cliente ao banco de dados:
        modelCliente.setFone_cli(this.txtclientefone.getText());
        modelCliente.setNome_cli(this.txtclientenome.getText());
        modelCliente.setCelular_cli(this.txtclientecelular.getText());
        modelCliente.setEmail_cli(this.txtclienteemail.getText());
        modelCliente.setEnd_cli(this.txtclienteendereco.getText());
        modelCliente.setComplemento(this.txtclientecomplemento.getText());
        modelCliente.setBairro(this.txtclientebairro.getText());
        modelCliente.setCidade(this.txtclientecidade.getText());
        modelCliente.setEstado(this.comboclienteestado.getSelectedItem().toString());
        modelCliente.setRG_cli(this.txtclienterg.getText());
        modelCliente.setCpf_cli(this.txtclientecpf.getText());

        //Condição para testar se houve sucesso ou erro no cadastro:
        if (controllerCliente.salvarClienteController(modelCliente) > 0) {
            JOptionPane.showMessageDialog(this, "Cliente cadastrado com sucesso!");
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao cadastrar cliente!", "ERRO", JOptionPane.ERROR_MESSAGE);
        }

        // Setar campos como nulo:
        txtclientebusca.setText(null);
        txtclientecpf.setText(null);
        txtclienteemail.setText(null);
        txtclienteendereco.setText(null);
        txtclientefone.setText(null);
        txtclienteid.setText(null);
        txtclientenome.setText(null);
        txtclientebairro.setText(null);
        txtclientecelular.setText(null);
        txtclientecomplemento.setText(null);
        txtclienterg.setText(null);
        comboclienteestado.setSelectedItem(null);
        txtclientecidade.setText(null);

        // Chamar o método de busca de clientes da tabela:
        busca_cliente();
    }//GEN-LAST:event_btn_CadastrarActionPerformed

