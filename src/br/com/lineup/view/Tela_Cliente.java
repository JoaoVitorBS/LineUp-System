    // Chamar o método de busca de clientes da tabela:
    private void busca_cliente() {
        listaModelClientes = controllerCliente.getListaClienteController();
        DefaultTableModel tabela = (DefaultTableModel) tabelacliente.getModel();
        tabela.setNumRows(0);

        int tam_lista = listaModelClientes.size();
        for (int i = 0; i < tam_lista; i++) {
            tabela.addRow(new Object[]{
                listaModelClientes.get(i).getId_cli(),
                listaModelClientes.get(i).getNome_cli(),
                listaModelClientes.get(i).getFone_cli(),
                listaModelClientes.get(i).getCelular_cli(),
                listaModelClientes.get(i).getEmail_cli(),
                listaModelClientes.get(i).getEnd_cli(),
                listaModelClientes.get(i).getComplemento(),
                listaModelClientes.get(i).getBairro(),
                listaModelClientes.get(i).getCidade(),
                listaModelClientes.get(i).getEstado(),
                listaModelClientes.get(i).getRG_cli(),
                listaModelClientes.get(i).getCpf_cli(),});

        }
    }

