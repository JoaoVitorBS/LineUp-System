    private void rSButtonMetro3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rSButtonMetro3ActionPerformed
        int del = tabelausuario.getSelectedRow();
        int id_user = (int) tabelausuario.getValueAt(del, 0);
        int confirma = JOptionPane.showConfirmDialog(null, "Deseja deletar este Usuário?", "Atenção", JOptionPane.YES_NO_OPTION);
        if (confirma == JOptionPane.YES_OPTION) {
            //Condição para testar se houve sucesso ou erro na exclusão:
            try {
                if (controllerUsuário.excluirUsuárioController(id_user)) {
                    JOptionPane.showMessageDialog(this, "Usuário deletado com sucesso!");
                } else {
                    JOptionPane.showMessageDialog(this, "Erro ao tentar deletar usuário!", "ERRO", JOptionPane.ERROR_MESSAGE);
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
        // Setando os campos como nulo:
        txt_iduser.setText(null);
        txt_nomeuser.setText(null);
        txt_foneuser.setText(null);
        txt_loginuser.setText(null);
        txt_senhauser.setText(null);
        combo_perfil.setSelectedItem(null);

        // Chamando o método de consulta dos usuários:
        busca_usuario();

    }//GEN-LAST:event_rSButtonMetro3ActionPerformed

