    private void busca_usuario() {
        listaModelUsuários = controllerUsuário.getListaUsuárioController();
        DefaultTableModel tabela = (DefaultTableModel) tabelausuario.getModel();
        tabela.setNumRows(0);

        int tam_lista = listaModelUsuários.size();
        for (int i = 0; i < tam_lista; i++) {
            tabela.addRow(new Object[]{
                listaModelUsuários.get(i).getIduser(),
                listaModelUsuários.get(i).getNomeuser(),
                listaModelUsuários.get(i).getFoneuser(),
                listaModelUsuários.get(i).getLogin(),
                listaModelUsuários.get(i).getSenha(),
                listaModelUsuários.get(i).getPerfil(),});
        }
    }

