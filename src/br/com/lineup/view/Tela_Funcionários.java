    private void rSButtonMetro2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rSButtonMetro2ActionPerformed
        // Adicionando dados do usuário no banco a partir das informações das caixas de texto:
        modelUsuário.setNomeuser(this.txt_nomeuser.getText());
        modelUsuário.setFoneuser(this.txt_foneuser.getText());
        modelUsuário.setLogin(this.txt_loginuser.getText());
        modelUsuário.setSenha(this.txt_senhauser.getText());
        modelUsuário.setPerfil(this.combo_perfil.getSelectedItem().toString());

        if (controllerUsuário.salvarUsuárioController(modelUsuário) > 0) {
            JOptionPane.showMessageDialog(this, "Usuário cadastrado com sucesso!");
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao tentar cadastrar Usuário!", "ERRO", JOptionPane.ERROR_MESSAGE);
        }

        // Setando os campos como nulo:
        txt_iduser.setText(null);
        txt_nomeuser.setText(null);
        txt_foneuser.setText(null);
        txt_loginuser.setText(null);
        txt_senhauser.setText(null);
        combo_perfil.setSelectedItem(null);

        // Chamando o método de consulta dos usuários:
        busca_usuario();
    }//GEN-LAST:event_rSButtonMetro2ActionPerformed

