    private void btn_Adicionar_itemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_Adicionar_itemActionPerformed
        // Estabelecendo as restriçoes para adicionar os itens na tabela
        if (txt_Id_Prod.getText().equals("")) {
            if (txt_nome_prod.getText().equals("Selecione um Produto...")) {
                if (txt_Qtde_Prod.getText().equals("")) {
                    if (txt_Valor_Un.getText().equals("")) {
                        JOptionPane.showMessageDialog(null, "Nenhum produto selecionado, Favor selecionar um produto!", "ATENÇÃO", JOptionPane.WARNING_MESSAGE);
                    }
                }
            }
        } else {
            if (txt_Qtde_Prod.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Campo Quantidade não pode conter letras ou ser deixado em branco!", "ATENÇÃO", JOptionPane.WARNING_MESSAGE);
            } else {
                DefaultTableModel produtos = (DefaultTableModel) tabelaprodutos.getModel();
                Object[] dados = {txt_Id_Prod.getText(), txt_nome_prod.getText(), txt_Qtde_Prod.getText(), txt_Valor_Un.getText(), txt_ValorTotal_Prod.getText()};
                produtos.addRow(dados);

                //Setar campos como Nulo (Limpar Campos)
                txt_Id_Prod.setText(null);
                txt_nome_prod.setText(null);
                txt_Qtde_Prod.setText(null);
                txt_Valor_Un.setText(null);
                txt_ValorTotal_Prod.setText(null);
            }
        }
        txt_ValorTotal_Venda.setText(Calcular_Produtos());
        lbl_Itens.setText(Calcular_Itens());
        txt_nome_prod.setText("Selecione um Produto...");
    }//GEN-LAST:event_btn_Adicionar_itemActionPerformed

