    private String Calcular_Produtos() {
        Double soma = 0.0;
        for (int i = 0; i < tabelaprodutos.getRowCount(); i++) {
            soma += Double.parseDouble(tabelaprodutos.getValueAt(i, 4).toString());
        }
        return soma.toString();
    }

    private String Calcular_Itens() {
        Double item = 0.0;
        for (int i = 0; i < tabelaprodutos.getRowCount(); i++) {
            item += Double.parseDouble(tabelaprodutos.getValueAt(i, 2).toString());
        }
        return item.toString();
    }

